package lk.furniture.store.dto;

/**
 *
 * @author Lakitha
 */
public class CustomerDTO extends SuperDTO{
    
    private String CID;
    private String FirstName;
    private String LastName;
    private String address;
    private String district;
    private String contactNumber;

    public CustomerDTO() {
    }

    public CustomerDTO(String CID, String FirstName, String LastName, String address, String district, String contactNumber) {
        this.CID = CID;
        this.FirstName = FirstName;
        this.LastName = LastName;
        this.address = address;
        this.district = district;
        this.contactNumber = contactNumber;
    }

    

    public CustomerDTO(String Contact) {
        this.contactNumber= Contact;
    }

    /**
     * @return the CID
     */
    public String getCID() {
        return CID;
    }

    /**
     * @param CID the CID to set
     */
    public void setCID(String CID) {
        this.CID = CID;
    }

    /**
     * @return the fName
     */
    
    /**
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address the address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * @return the district
     */
    public String getDistrict() {
        return district;
    }

    /**
     * @param district the district to set
     */
    public void setDistrict(String district) {
        this.district = district;
    }

    /**
     * @return the contactNumber
     */
    public String getContactNumber() {
        return contactNumber;
    }

    /**
     * @param contactNumber the contactNumber to set
     */
    public void setContactNumber(String contactNumber) {
        this.contactNumber = contactNumber;
    }

    /**
     * @return the FirstName
     */
    public String getFirstName() {
        return FirstName;
    }

    /**
     * @param FirstName the FirstName to set
     */
    public void setFirstName(String FirstName) {
        this.FirstName = FirstName;
    }

    /**
     * @return the LastName
     */
    public String getLastName() {
        return LastName;
    }

    /**
     * @param LastName the LastName to set
     */
    public void setLastName(String LastName) {
        this.LastName = LastName;
    }

    

    }
